#PROGRAMA QUE PERGUNTE A HORA AO USUÁRIO E BASEANDO SE NO HORARIO, RESPONDA COM A SAUDAÇÃO ADEQUADA.

horario = input(('Digite um horario.'))

if horario.isdigit():
  horario = int(horario)

  if horario < 0 or horario > 23:
    print('Digite um numero válido entre 0 e 23.')

  else:
    if horario <= 11:
      print('Bom dia!')

    elif horario <= 17:
      print('Boa tarde!')

    elif horario >= 18:
      print('Boa noite!')
